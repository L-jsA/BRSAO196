#LIÇÃO 2 - TIPO DE DADOS NUMÉRICOS
numero = 1 #número inteiro
print (numero) #imprime o número no terminal
print (type(numero)) #mostra o tipo de dado (número)
print(str(numero) + " é do tipo de dado string " + str(type(numero))) #converte um número inteiro para uma string e mostra o tipo de dado

decimal = 3.14 #número decimal (float)
print (decimal) #imprime o número no terminal
print (type(decimal)) #mostra o tipo de dado (float)
print(str(decimal) + " é do tipo de dado decimal " + str(type(decimal))) #converte um número inteiro para uma float e mostra o tipo de dado

complexo = 5j #número complexo(complex)
print (complexo) #imprime o número no terminal
print (type(complexo)) #mostra o tipo de dado (complex)
print(str(complexo) + " é do tipo de dado complexo " + str(type(complexo))) #converte um número para string e mostra o tipo de dado

verdadeiro = True #Exemplo de booleano (true)
print (verdadeiro) #imprime o booleano no terminal
print (type(verdadeiro)) #mostra o tipo de dado (booleano)
print(str(verdadeiro) + " é do tipo de dado booleano" + str(type(verdadeiro))) #converte o dado para string e mostra o tipo de dado

falso = False #Exemplo de booleano (falso)
print (falso) #imprime o booleano no terminal
print (type(falso)) #mostra o tipo de dado (booleano)
print(str(falso) + " é do tipo de dado booleano" + str(type(falso))) #converte o dado para string e mostra o tipo de dado

nome = "Luana" #Exemplo de texto(string)
print (nome) #imprime a string no terminal
print (type(nome)) #mostra o tipo de dado (string)
print(str(nome) + " é do tipo de dado string" + str(type(nome))) #converte o dado para string e mostra o tipo de dado