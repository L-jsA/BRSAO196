# FUNÇÕES

# FUNÇÕES COM PARÂMETRO

def dobrar(numero): # A função dobrar recebe um número e retorna o dobro dele.
    return numero * 2

# Exemplo de uso:
resultado = dobrar(5) # Você passa o valor ao chamar a função: dobrar(5)
print("Resultado:", resultado) 

# FUNÇÕES SEM PARÂMETRO
def mostrar_mensagem(): # A função mostrar_mensagem não precisa de nenhum dado externo (parâmetro).
    print("Olá! Esta é uma mensagem padrão.")

# Exemplo de uso:
mostrar_mensagem()  # Saída: Olá! Esta é uma mensagem padrão. Ela apenas executa uma ação ao ser chamada.